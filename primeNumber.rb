## Write a function to check if a number is a prime number


def PrimeNumber(number)
  if number%2 == 0
  	puts "False, your number is not a Prime Number"
  else
  	puts "True, your number is a Prime Number"
  end
end

PrimeNumber(5)
PrimeNumber(6)
PrimeNumber(1)
PrimeNumber(7)
PrimeNumber(13)
